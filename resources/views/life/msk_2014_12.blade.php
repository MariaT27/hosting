  <img src="//life.ivacuum.ru/msk.2014.12/IMG_1357.jpg" width="1000" height="750" data-map-lat="55.7694055556" data-map-lon="37.5958583333">
</div>

<p>Спешить было некуда, группа малоизвестная, площадка небольшая, можно было погулять по окрестностям. В первом же попавшемся парке деревья оформлены гораздо гуще, чем в Калуге. Тем не менее акцент на стволах скорее напоминает выгоревший лес.</p>
<div class="img-container">
  <img src="//life.ivacuum.ru/msk.2014.12/IMG_1358.jpg" width="1000" height="750">
</div>

<a name="house_number"></a>
<p>Нашлась табличка с номером дома 28-30. Это означает, что прежде на этом месте было два дома: 28 и 30. Потом их снесли, а на их месте построили один новый. Кстати, номер корпуса добавляют зданию, если оно находится во дворе, то есть не имеет прямого выхода на улицу. Букву, если здание строят между двумя имеющимися. Номер строения, если здания имеют общий въезд с улицы. Ради интереса посмотрите сколько строений по адресу: <a href="http://maps.yandex.ru/-/CVC6mOpe" class="link">Москва, улица Автозаводская, дом 23</a>. Опытный читатель найдет, например, строение 998.</p>
<div class="img-container">
