<p>Свернув в один из переулков, наткнулся на ресторан из русского сериала Кухня. Там он назывался Клод Моне. Ранее в этом же году в Калуге по пути с катка встретил актера Дмитрия Назарова, который исполнял в данном сериале роль шеф-повара.</p>
